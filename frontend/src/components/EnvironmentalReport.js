const CircularEHS = ({ value }) => {
  const radius = 50;
  const circumference = 2 * Math.PI * radius;
  const offset = circumference - (value / 100) * circumference;

  // Dynamic color based on score
  const getColor = (v) => {
    if (v >= 80) return "stroke-green-500 text-green-500";
    if (v >= 60) return "stroke-yellow-500 text-yellow-500";
    if (v >= 40) return "stroke-orange-500 text-orange-500";
    return "stroke-red-500 text-red-500";
  };

  return (
    <div className="relative w-[120px] h-[120px] flex items-center justify-center">
      <svg
        className="transform -rotate-90"
        width="120"
        height="120"
        viewBox="0 0 120 120"
      >
        {/* Background circle */}
        <circle
          cx="60"
          cy="60"
          r={radius}
          stroke="#e5e7eb"
          strokeWidth="10"
          fill="none"
        />
        {/* Foreground progress circle */}
        <circle
          cx="60"
          cy="60"
          r={radius}
          className={getColor(value)}
          strokeWidth="10"
          fill="none"
          strokeDasharray={circumference}
          strokeDashoffset={offset}
          strokeLinecap="round"
          style={{ transition: "stroke-dashoffset 1s ease" }}
        />
      </svg>

      {/* Text inside circle */}
      <div className="absolute text-center">
        <p className="text-2xl font-bold text-gray-800">{value}%</p>
        <p className="text-sm text-gray-500">EHS Score</p>
      </div>
    </div>
  );
};

